# 假设以下内容是从网络上抓取的
# 要求：顺序并且居中对齐输出以下内容
poem = ["登鹳鹤楼\t\r",
        "\t\n王之涣\r",
        "\t\n      白日依山尽",
        "黄河入海流",
        "欲穷千里目",
        "更上一层楼"]
print("|%s|" % ("－" * 16))

for line in poem:
    # 先使用strip 方法去除字符串中的空白字符
    # 再使用center方法居中显示文本
    print("|%s|" % line.strip().center(16, "　"))  # 格式化字符串的长度为16(包含原有的字符和填充字符）

print("|%s|" % ("＿" * 16))

