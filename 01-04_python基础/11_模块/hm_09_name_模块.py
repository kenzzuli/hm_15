# 模块向外提供 全局变量、函数、类，而直接执行的代码，并不是模块向外提供的工具


# 1.小明开发的函数
def say_hello():
    print("你好你好！")


def main():
    # main函数中包含一系列测试代码

    # 如果直接执行模块 输出 __main__
    # 如果被其他模块调用 输出调用该模块的模块的名称 输出 hm_09_name_模块
    print(__name__)

    # 模块被导入时，能够直接执行的代码不需要被执行！
    print("小明开发的模块")

    # 2.小明在模块中测试自己开发的函数
    say_hello()


# 增加一个判断，只有当该模块自己执行时，才执行测试代码，如果是被别的模块导入，则不会执行测试代码
if __name__ == "__main__":
    main()
